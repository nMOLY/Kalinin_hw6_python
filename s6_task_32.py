# Задача 32: Определить индексы элементов массива (списка), значения которых принадлежат 
# заданному диапазону (т.е. не меньше заданного минимума и не больше заданного максимума)

# Ввод данных с клавиатуры
n = int(input("Введите количество элементов в массиве: "))
min_value = int(input("Введите минимальное значение диапазона: "))
max_value = int(input("Введите максимальное значение диапазона: "))

# Создание и заполнение массива
array = []
for i in range(n):
    element = int(input("Введите элемент массива: "))
    array.append(element)

# Поиск элементов, удовлетворяющих условию
indices = []
for i in range(n):
    if min_value <= array[i] <= max_value:
        indices.append(i)

# Вывод результатов
if len(indices) == 0:
    print("Нет элементов, удовлетворяющих условию")
else:
    print("Индексы элементов, удовлетворяющих условию:")
    for index in indices:
        print(index)